import numpy as np

# Создание массива чисел от 1 до 10
array = np.arange(1, 11)
print("Исходный массив:", array)

# Выполнение математических операций
array_sum = np.sum(array)           # Сумма элементов массива
array_mean = np.mean(array)         # Среднее значение элементов массива
array_squared = np.square(array)    # Квадрат каждого элемента
array_sqrt = np.sqrt(array)         # Квадратный корень каждого элемента

# Вывод результатов
print("Сумма элементов массива:", array_sum)
print("Среднее значение элементов массива:", array_mean)
print("Квадрат каждого элемента массива:", array_squared)
print("Квадратный корень каждого элемента массива:", array_sqrt)

